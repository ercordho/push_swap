#include "../../inc/push_swap.h"

double	ft_nbrlen(double nbr)
{
	double	i;

	i = 0;
	if (nbr < 0)
		nbr *= -1;
	while (nbr > 0)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
